
public class 반복문_2 {
	public static void main(String[] args) {
		
		for(int i =0; i<=10;i++) {
			System.out.print(i +" ");
			
		}
		System.out.println();
		for(int i =10; i>=1;i--) {
			System.out.print(i +" ");
			
		}
		for (char c = 'A';c<='Z';c++ ) {
			System.out.print(c);
			
		}
	}

}
