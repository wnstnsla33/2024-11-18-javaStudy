
public class 반복문_5 {
	public static void main(String[] args) {
		int sum =0;
		for(int i=1;i<=100;i+=3) {
			sum+=i;
			
		}
		System.out.println(sum);
		sum =0;
		for(int i=0;i<=100;i+=5) {
			sum+=i;
			
		}
		System.out.println(sum);
		sum =0;
		for(int i=0;i<=100;i+=7) {
			sum+=i;
			
		}
		System.out.println(sum);
	}
	
	
	
}
